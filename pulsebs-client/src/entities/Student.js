class Student{

    constructor(id, name, surname, email, city, bday, ssn, password) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.city = city;
        this.bday = bday;
        this.ssn = ssn;
        this.password = password;
    }
}

export default Student;